package bolum02;

public class Literaller {
	public static void main(String[] args) {
		
		byte b = 127;
		
		short s = 32000;
		
		int i = 2147000000;
		System.out.println(i);
		
		long l = 900000000000L;
		
		float f = 1.0F;
		
		double d = 1.0;


		// cok cok az kullanilir
		System.out.println(0B1111); // Displays 15
		System.out.println(07777); // Displays 4095
		System.out.println(0XFFFF); // Displays 65535
		
		System.out.println(1.23456E300);
		
		
	}
}
